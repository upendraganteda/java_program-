import java.lang.String;
import java.util.Scanner;  
class upto  
{  
public static void main(String[] args)   
{  
int number, i;  
Scanner sc=new Scanner(System.in);  
System.out.print("Enter the limit: ");  
number = sc.nextInt();    
i=2;   
  
while(i<=number)  
{  
  i=i+2;
  System.out.print(i+"");
 
}     
} 
}    